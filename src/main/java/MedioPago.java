public class MedioPago {
	private float _dinero;
	private int _tarjeta;
	private String _cheque;

	public float getDinero() {
		return this._dinero;
	}

	public void setDinero(float aDinero) {
		this._dinero = aDinero;
	}

	public int getTarjeta() {
		return this._tarjeta;
	}

	public void setTarjeta(int aTarjeta) {
		this._tarjeta = aTarjeta;
	}

	public String getCheque() {
		return this._cheque;
	}

	public void setCheque(String aCheque) {
		this._cheque = aCheque;
	}
}